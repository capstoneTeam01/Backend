const normalizeToolName = (value) => {
  return String(value || "")
    .trim()
    .toLowerCase()
    .replace(/[-_/]+/g, " ")
    .replace(/[^a-z0-9' ]+/g, " ")
    .replace(/\s+/g, " ");
};

const NORMALIZED_TOOL_IMAGES = Object.entries(
  TOOL_IMAGES
)
  .map(([key, tool]) => ({
    key: normalizeToolName(key),
    tool,
  }))
  .sort(
    (first, second) =>
      second.key.length - first.key.length
  );

const findToolImage = (normalizedName) => {
  const paddedName = ` ${normalizedName} `;

  return NORMALIZED_TOOL_IMAGES.find(
    ({ key }) =>
      normalizedName === key ||
      paddedName.includes(` ${key} `)
  )?.tool;
};

const mapToolsWithImages = (tools = []) => {
  if (!Array.isArray(tools)) {
    return [];
  }

  return tools.map((tool) => {
    const toolName =
      typeof tool === "string"
        ? tool
        : tool?.name || "Tool";

    const normalizedName =
      normalizeToolName(toolName);

    const matchedTool =
      findToolImage(normalizedName);

    return {
      name: toolName,
      imageUrl:
        matchedTool?.imageUrl || null,
    };
  });
};

export { mapToolsWithImages };
